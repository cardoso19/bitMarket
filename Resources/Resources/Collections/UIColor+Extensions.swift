import UIKit

public extension UIColor {
    static var primary: UIColor? { return UIColor(named: "primary") }
    static var secondary: UIColor? { return UIColor(named: "secondary") }
    static var tertiary: UIColor? { return UIColor(named: "tertiary") }
    static var queternary: UIColor? { return UIColor(named: "queternary") }
}
